package loops;

public class loopPractice2 {
    /*
    there are few type of loops
    1. while loop
    2. For Loop
    3. Do- while Loop
     */


    public static  void whileLoop(){
//        1-100 print
        int a = 1;
        while (a<=100){

            System.out.println(a);
        }
        a++;


    }


    public static void forLoop(){

        for (int a = 1; a<=100;a++){
            System.out.println(a);

        }


    }



}
